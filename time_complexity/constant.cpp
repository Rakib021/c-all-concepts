#include<bits/stdc++.h>
  
using namespace std;  

/*
time complexity = O(1);
memory complexity =O(1)
*/
  
int main () {  
  cout << "max(1,2)==" << max(1,2) << '\n';  
  cout << "max(2,1)==" << max(2,1) << '\n';  
  cout << "max('a','z')==" << max('a','z') << '\n';  
  cout << "max(3.14,2.73)==" << max(3.14,2.73) << '\n';  
  return 0;  
}  